
public class Variable {

	public static void main(String[] args) {
		//변수안에 어떤 데이터타입 들어갈지 지정해줘야됨
		int a = 1; 
		// integer -2, -1, 0, 1...
		System.out.println(a);
		double b = 1.1;
		// double real number 실수 -2.0, -1.1, 2.3...
		System.out.println(b);

		String c = "Hello";
		System.out.println(c);
	}

}

//자바와같은 컴퓨터언어는 변수를 지정해줘야됨
//자바 변수에는 정의를 해놓으면 그 데이터 반드시 문자나 정수 .. 아닌정보가 들어갈때 자바가 아예 컴파일 하지 않음
//값을 넣을땐 까다롭지만 그 안에있는건 확실히 반든시 정수나 문자로 확
 